var pic = document.getElementById("imglevi")

function changePic() { 
    
    pic.setAttribute('src', 'leviphotonobg.png')
    pic.style.marginLeft = '20%'
    pic.style.marginTop = '15%'
    pic.style.width = "300px"  
}
  
  


function changePicBack() {
     
    pic.setAttribute('src', 'leviorlando.png')
    pic.style.marginTop = '0%'
    pic.style.width = '500px'
    pic.style.marginLeft = '0%'
}
   








